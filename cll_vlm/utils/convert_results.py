import json
import csv
import os
import pickle
import argparse

def preprocess_label(label: str) -> str:
    """
    Preprocess label to match the format used in the description file.
    Handles specific overrides for CIFAR-20/100 and general underscores.
    """
    if label is None:
        return ""
    if label.startswith("vehicles_1"):
        return "transportation vehicles"
    if label.startswith("vehicles_2"):
        return "industrial and military vehicles"
    return label.replace("_", " ").strip().lower()

def load_label_names(data_root, data_name):
    """
    Load label names based on dataset.
    """
    if data_name == "cifar10":
        meta_path = os.path.join(data_root, 'cifar-10-batches-py', 'batches.meta')
        if not os.path.exists(meta_path):
            raise FileNotFoundError(f"Metadata file not found at {meta_path}")
        with open(meta_path, 'rb') as f:
            meta = pickle.load(f)
        return meta['label_names']
    
    elif data_name in ["cifar20", "cifar100"]:
        # cifar20 uses the same root as cifar100 but uses different labels
        meta_path = os.path.join(data_root, 'cifar-100-python', 'meta')
        if not os.path.exists(meta_path):
            raise FileNotFoundError(f"Metadata file not found at {meta_path}")
        with open(meta_path, 'rb') as f:
            meta = pickle.load(f)
        if data_name == "cifar20":
            return meta['coarse_label_names']
        else:
            return meta['fine_label_names']
            
    elif data_name == "tiny200":
        wnids_path = os.path.join(data_root, 'wnids.txt')
        words_path = os.path.join(data_root, 'words.txt')
        
        if not os.path.exists(wnids_path) or not os.path.exists(words_path):
            raise FileNotFoundError(f"Tiny200 metadata files (wnids.txt, words.txt) not found at {data_root}")
            
        with open(wnids_path, 'r') as f:
            wnids = [line.strip() for line in f.readlines()]
            
        wnid_to_words = {}
        with open(words_path, 'r') as f:
            for line in f.readlines():
                parts = line.strip().split('\t')
                if len(parts) >= 2:
                    wnid_to_words[parts[0]] = parts[1]
                    
        classes = []
        for wnid in wnids:
            if wnid in wnid_to_words:
                class_name = wnid_to_words[wnid].split(',')[0].strip()
                classes.append(class_name)
            else:
                classes.append(wnid)
        return classes
    
    else:
        raise ValueError(f"Unsupported dataset: {data_name}")

def main():
    parser = argparse.ArgumentParser(description="Convert JSON results to CSV with label mapping.")
    parser.add_argument("--input_json", type=str, required=True, help="Path to input JSON file.")
    parser.add_argument("--output_csv", type=str, help="Path to output CSV file.")
    parser.add_argument("--data_name", type=str, required=True, choices=["cifar10", "cifar20", "cifar100", "tiny200"], help="Name of the dataset.")
    parser.add_argument("--data_root", type=str, required=True, help="Root directory for the dataset metadata.")
    
    args = parser.parse_args()

    # Determine output path if not provided
    if args.output_csv is None:
        input_dir = os.path.dirname(os.path.abspath(args.input_json))
        output_dir = os.path.join(input_dir, "csv")
        os.makedirs(output_dir, exist_ok=True)
        
        base_name = os.path.splitext(os.path.basename(args.input_json))[0]
        args.output_csv = os.path.join(output_dir, f"{base_name}.csv")
        print(f"Output path autogenerated: {args.output_csv}")
    
    # Ensure output directory exists
    os.makedirs(os.path.dirname(os.path.abspath(args.output_csv)), exist_ok=True)

    # 1. Load Label Mappings
    try:
        classes = load_label_names(args.data_root, args.data_name)
        print(f"Loaded {len(classes)} classes for {args.data_name}")
    except Exception as e:
        print(f"Error loading metadata: {e}")
        return

    # Create mappings
    # Map preprocessed name to index
    preprocessed_name_to_idx = {preprocess_label(name): i for i, name in enumerate(classes)}
    # Map original name to index (for safety)
    name_to_idx = {name.strip().lower(): i for i, name in enumerate(classes)}

    def get_label_index(label_name):
        if label_name is None:
            return -1
        
        target = label_name.strip().lower()
        
        # Try direct match
        if target in name_to_idx:
            return name_to_idx[target]
        
        # Try preprocessed match
        prep_name = preprocess_label(label_name)
        if prep_name in preprocessed_name_to_idx:
            return preprocessed_name_to_idx[prep_name]
            
        print(f"Warning: Could not map label '{label_name}' to an index.")
        return -1

    # 2. Read JSON
    with open(args.input_json, 'r') as f:
        data = json.load(f)
    
    print(f"Loaded {len(data)} samples from {args.input_json}")

    # 3. Process and Write CSV
    with open(args.output_csv, 'w', newline='') as csvfile:
        fieldnames = ['img_idx', 'true_label', 'shuffle_label', 'flag']
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        
        writer.writeheader()
        
        for item in data:
            img_idx = item.get('img_idx')
            true_label_str = item.get('true_label')
            shuffle_label_str = item.get('shuffled_label')
            answer = item.get('answer')
            
            # Map labels to indices
            true_label_idx = get_label_index(true_label_str)
            shuffle_label_idx = get_label_index(shuffle_label_str)
            
            # Map answer to flag
            flag = "ol" if answer == "YES" else "cl"
            
            row = {
                'img_idx': img_idx,
                'true_label': true_label_idx,
                'shuffle_label': shuffle_label_idx,
                'flag': flag
            }
            
            writer.writerow(row)
            
    print(f"Successfully wrote {len(data)} rows to {args.output_csv}")

if __name__ == "__main__":
    main()

